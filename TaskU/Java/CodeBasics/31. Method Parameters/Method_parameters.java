public class Method_parameters {
    public static void main(String[] args) {
        var one = "Naharis";
        var two = "Mormont";
        var three = "Sand";

        var first = one.charAt(2);
        var second = two.charAt(1);
        var third = three.charAt(3);
        var fourth = two.charAt(4);
        var fifth = two.charAt(2);
        System.out.println(first + second + third + fourth + fifth);
    }
}